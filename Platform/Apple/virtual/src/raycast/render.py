def log2_w_w(n):
  if (n == 0):
    return 0

  # Calculate the exponent, and leave mantissa in n.
  exp = 8
  while (n >= 512):
    exp += 1
    n >>= 1

  while (n < 256):
    exp -= 1
    n <<= 1

  # Combine to form the result
  return round((exp << 8) | tbl_log2_w_w[n & 0xFF])


